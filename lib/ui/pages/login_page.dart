  void _onSignInButtonPress() {

   showInSnackBar(loginEmailController.text.length.toString(), Colors.green);

    if(loginEmailController.text.length<=0){
     showInSnackBar("Invalid email", Colors.red);
    }
    else if(loginPasswordController.text.length<=0){
     showInSnackBar("Invalid password", Colors.red);
    }
    else{
      userLogin(loginEmailController.text, loginPasswordController.text);
    }
  }

  void _toggleLogin() {
    setState(() {
      _obscureTextLogin = !_obscureTextLogin;
    });
  }

  void userLogin(username, password){
    
   showInSnackBar("Please wait!", Colors.blue);

    checkUserLogin(username, password).then((onValue) async
    {
      dynamic resultArray = new JsonDecoder().convert(onValue.body);

      print(onValue.body);

      User us = User.fromJson(resultArray[0]['data']);
      us.password = password;

      PreferenceManager.saveUserData(us);

      PreferenceManager
          .getUserData()
      .then((u){

        if(int.parse(u.id)>0){
          Navigator.of(context).pushNamed('/dashboard');
        }
        else{
          showInSnackBar("Unable to login", Colors.red);
        }

      });

    }).catchError((onError){
      showInSnackBar(onError.toString(), Colors.red);
    });

  }

  static Future<http.Response> checkUserLogin(username, password) async {
    final response =
    await http.post(url,body: {
      "user_name": username,
      "password": password,
      "method": "user_login"
    });
    return response;

  }
